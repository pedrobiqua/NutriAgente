              <li class="card-text mb-auto">Lave bem as berinjelas, retire as duas pontas e corte em fatias finas.</li>
              <li class="card-text mb-auto">Em uma frigideira, coloque o azeite e frite as fatias dos dois lados, até ficarem douradas.</li>
              <li class="card-text mb-auto">Em um processador ou liquidificador, coloque o tofu, triture e coloque em uma peneira.</li>
