              <li class="card-text mb-auto">Coloque uma fatia de berinjela, acrescente um pedaço de tomate confitado e enrole.</li>
              <li class="card-text mb-auto">Acomode em uma travessa, regue com o azeite dos tomates confitados e sirva.</li>
